

-- RESUMO PEDIDO DE VENDAS

SELECT A1_FILIAL,A1_COD,A1_NOME,C6_VALOR FROM SA1990 SA1
INNER JOIN SC5990 SC5 ON SA1.A1_COD = C5_CLIENTE AND A1_LOJA = C5_LOJACLI
INNER JOIN SC6990 SC6 ON C6_NUM = C5_NUM
WHERE SC5.D_E_L_E_T_ = ''



SELECT A1_COD,A1_NOME,A1_LOJA,SUM(C6_VALOR)SOMA_VALORES,COUNT(C5_NUM)QUANTIDADE_PEDIDOS,SUM(C6_QTDVEN) QUANTIDADE_PRODUTO FROM SA1990 SA1
INNER JOIN SC5990 SC5 ON SA1.A1_COD = C5_CLIENTE AND A1_LOJA = C5_LOJACLI
INNER JOIN SC6990 SC6 ON C6_NUM = C5_NUM
INNER JOIN SB1990 SB1 ON B1_COD = C6_PRODUTO
WHERE SC5.D_E_L_E_T_ = ''
GROUP BY A1_COD,A1_NOME,A1_LOJA










